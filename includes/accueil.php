      <p> Au centre de la plupart des galaxies, on trouve l'une des choses les plus étranges et les plus meurtrières de l'univers : un trou noir.</p>

<p>La plupart des trous noirs, quelle que soit leur taille, apparaissent lorsqu'une étoile géante manque d'énergie. L'étoile implose et son centre s'effondre sous son propre poids. Cela provoque une explosion appelée une supernova (pour voir à quoi ressemble une supernova, regarde la vidéo ci-dessous !). L'étoile géante est finalement réduite en une bille très compacte que l'on ne peut pas voir à l'oeil nu.

La gravité d'un trou noir, ou sa force d'attraction, est si forte qu'elle attire tout ce qui s'en approche d'un peu trop près. Le trou noir peut même avaler des étoiles entières. Rien ne peut se déplacer assez vite pour échapper à la gravité d'un trou noir. Même pas la lumière, chose la plus rapide de l'univers. C'est pourquoi nous ne pouvons pas observer de trous noirs dans l'espace : ils ont englouti toute la lumière. Bien que les astronomes ne puissent pas voir les trous noirs, ils savent qu'ils sont là par l'effet qu'ils ont sur les objets qui s'en approchent un peu trop.</P>
