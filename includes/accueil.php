<p>

Il existe deux types de trous noirs :

<li>⭐ Les trous noirs supermassifs sont les plus grands de tous les trous noirs. Leur masse est jusqu'à un million de fois plus importante que celle de notre soleil. Par exemple, le trou noir qui est au centre de notre galaxie, la Voie Lactée, est un trou noir supermassif. Il s'appelle Sagittaire A* (A* est le code scientifique pour "A-étoile").
</li>
<li>⭐ Les trous noirs les plus communs sont les trous noirs stellaires. Leur masse ne dépasse celle du Soleil que de 20 fois.
</li>
</p>
<p>Les rémanents d'une extraordinaire Supernova.
LES RÉMANENTS D'UNE EXTRAORDINAIRE SUPERNOVA.
La gravité extrême près d'un trou noir déforme aussi le temps, qui peut se comporter de manière étrange. Si un astronaute quittait son vaisseau spatial pour explorer de près un trou noir, il verrait les aiguilles de sa montre avancer normalement. Mais si quelqu'un sur le vaisseau spatial pouvait observer la montre de l'astronaute de loin, ils verraient les aiguilles ralentir au fur et à mesure que l'il s'approcherait du trou noir. Quand l'astronaute reviendrait au vaisseau spatial après une heure dans l'espace, des années se seraient écoulées pour ceux qui étaient restés à bord du vaisseau spatial.

Un jour, les Hommes pourraient être en mesure d'utiliser des trous noirs pour voyager dans le temps. Un astronaute pourrait faire un court trajet près d'un trou noir et retourner sur Terre après des années, des décennies ou même des siècles. Une machine à remonter le temps spatiale qui pourrait nous permettre de découvrir à quoi va ressembler le monde dans le futur. Mais remonter dans le temps ? C'est un défi tout à fait différent !
</p>
